type VanguardPatternBlueprint = {
  id: string;
  title: string;
  sourcePromptName: string;
  category: string;
  screenTypes: SpecularScreenType[];
  intentTerms: string[];
  dependencies: string[];
  componentPath?: string;
  signature: string;
  interaction: string;
  usage: string;
  fallback: string;
  contentRule?: string;
  mobile: string;
  mobileGuard: string;
};

function buildVanguardPattern(blueprint: VanguardPatternBlueprint): RetrobuilderVanguardPattern {
  return {
    id: blueprint.id,
    title: blueprint.title,
    sourcePromptName: blueprint.sourcePromptName,
    category: blueprint.category,
    screenTypes: blueprint.screenTypes,
    intentTerms: blueprint.intentTerms,
    dependencies: blueprint.dependencies,
    componentPath: blueprint.componentPath,
    designDna: [
      blueprint.signature,
      blueprint.interaction,
      blueprint.usage,
    ],
    implementationNotes: [
      blueprint.fallback,
      blueprint.contentRule || 'Replace demo labels, avatars, media, and copy with domain-specific product content.',
    ],
    mobileRules: [
      blueprint.mobile,
      blueprint.mobileGuard,
    ],
    stackAdapters: COMMON_STACK_ADAPTERS,
  };
}

