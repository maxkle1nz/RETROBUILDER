export type VanguardStackAdapter =
  | 'react-tailwind-shadcn'
  | 'vanilla-html-css-js'
  | 'vue'
  | 'svelte'
  | 'server-rendered-html';

export interface RetrobuilderVanguardPattern {
  id: string;
  title: string;
  sourcePromptName: string;
  category: string;
  screenTypes: SpecularScreenType[];
  intentTerms: string[];
  dependencies: string[];
  componentPath?: string;
  designDna: string[];
  implementationNotes: string[];
  mobileRules: string[];
  stackAdapters: Record<VanguardStackAdapter, string[]>;
}

