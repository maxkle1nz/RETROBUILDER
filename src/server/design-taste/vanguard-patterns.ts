function normalize(value: string) {
  return value.toLowerCase().replace(/[^a-z0-9]+/g, ' ').trim();
}

function tokenize(value: string) {
  return new Set(normalize(value).split(/\s+/).filter((part) => part.length >= 3));
}

function scorePattern(
  pattern: RetrobuilderVanguardPattern,
  node: Pick<SpecularNodeInput, 'label' | 'description' | 'type'> & Partial<Pick<SpecularNodeInput, 'data_contract' | 'acceptance_criteria' | 'error_handling'>>,
  screenType: SpecularScreenType,
) {
  const nodeText = [
    node.label,
    node.description || '',
    node.type || '',
    screenType,
    node.data_contract || '',
    ...(node.acceptance_criteria || []),
    ...(node.error_handling || []),
  ].join(' ');
  const nodeTokens = tokenize(nodeText);
  const patternTokens = tokenize([
    pattern.id,
    pattern.title,
    pattern.category,
    pattern.sourcePromptName,
    pattern.intentTerms.join(' '),
    pattern.designDna.join(' '),
    pattern.implementationNotes.join(' '),
    pattern.screenTypes.join(' '),
  ].join(' '));
  let score = pattern.screenTypes.includes(screenType) ? 42 : 16;

  for (const token of nodeTokens) {
    if (patternTokens.has(token)) score += 8;
  }
  for (const term of pattern.intentTerms) {
    if (normalize(nodeText).includes(term)) score += 10;
  }
  if (pattern.dependencies.length > 0) score += 4;
  if (pattern.componentPath) score += 3;
  return score;
}

